     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $inventarios = Inventario::join('productos','productos.id','=','inventarios.id_producto')->join('almacenes','almacenes.id','=','inventarios.id_almacen')
        ->where('inventarios.estado',1)->select('productos.id as producto','productos.nombre as nombre_producto','productos.cod_producto','almacenes.id as almacen',
        'almacenes.nombre as nombre_almacen','cantidad_min as min','cantidad_max as max','inventarios.id as id_inventario')->get();
        return view('partials.inventarios')->with('inventarios',$inventarios);
    }

