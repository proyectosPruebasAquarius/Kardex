    public function index()
    {
        $pedidos = Pedido::join('productos', 'pedidos_productos.id_producto', '=', 'productos.id')->join('proveedores', 'productos.id_proveedor', '=', 'proveedores.id')
        ->select('productos.nombre', 'productos.cod_producto', 'productos.id as producto', 'pedidos_productos.*', 'proveedores.nombre as proveedor', 'proveedores.telefono')->get();

        return view('layouts.pedidos')->with('data', $pedidos);
    }

